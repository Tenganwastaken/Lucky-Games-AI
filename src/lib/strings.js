const dateTimeFormatter = new Intl.DateTimeFormat('el-GR', {
  dateStyle: 'medium',
  timeStyle: 'short',
});

const timeFormatter = new Intl.DateTimeFormat('el-GR', {
  timeStyle: 'short',
});

const currencyFormatter = new Intl.NumberFormat('el-GR', {
  style: 'currency',
  currency: 'EUR',
});

export function formatDateTime(value) {
  const d = value instanceof Date ? value : new Date(value);
  if (Number.isNaN(d.getTime())) return '';
  return dateTimeFormatter.format(d);
}

export function formatTime(value) {
  const d = value instanceof Date ? value : new Date(value);
  if (Number.isNaN(d.getTime())) return '';
  return timeFormatter.format(d);
}

export function formatCurrency(amount) {
  if (amount == null || Number.isNaN(Number(amount))) return '—';
  return currencyFormatter.format(Number(amount));
}

/** Γραμμές tooltip αξιολόγησης στον χάρτη */
export function mapAdvisorHoverLines(stats, gameTypeKey) {
  if (!stats || (Number(stats.totalRuns) || 0) < 1) {
    return { title: null, lines: [] };
  }
  const totalRuns = Number(stats.totalRuns) || 0;
  const guestRuns = Number(stats.guestRuns) || 0;
  let distinctUsers = Number(stats.distinctUsers) || 0;
  if (guestRuns === totalRuns && totalRuns > 0) distinctUsers = 0;

  const gameLine = mapGameLabel(gameTypeKey);
  const lines = [];
  if (distinctUsers > 0) {
    lines.push(
      distinctUsers === 1
        ? '1 άτομο με λογαριασμό'
        : `${distinctUsers} άτομα με λογαριασμούς`,
    );
  }
  if (guestRuns > 0) {
    lines.push(
      guestRuns === 1
        ? '1 επίσκεψη επισκέπτη (χωρίς σύνδεση)'
        : `${guestRuns} επισκέψεις επισκεπτών (χωρίς σύνδεση)`,
    );
  }
  if (lines.length === 0) {
    lines.push(
      totalRuns === 1 ? '1 χρήση' : `${totalRuns} χρήσεις`,
    );
  }
  return { title: gameLine, lines };
}
